export function setColor(type: string) {
  if (type === 'bug') {
    return '#729f3f';
  } else if (type === 'dark') {
    return '#707070';
  } else if (type === 'dragon') {
    return '#f16e57';
  } else if (type === 'electric') {
    return '#eed535';
  } else if (type === 'fairy') {
    return '#fdb9e9';
  } else if (type === 'fighting') {
    return '#d56723';
  } else if (type === 'fire') {
    return '#fd7d24';
  } else if (type === 'flying') {
    return '#3dc7ef';
  } else if (type === 'ghost') {
    return '#7b62a3';
  } else if (type === 'grass') {
    return '#9bcc50';
  } else if (type === 'ground') {
    return '#f7de3f';
  } else if (type === 'ice') {
    return '#51c4e7';
  } else if (type === 'normal') {
    return '#a4acaf';
  } else if (type === 'poison') {
    return '#b97fc9';
  } else if (type === 'psychic') {
    return '#f366b9';
  } else if (type === 'rock') {
    return '#a38c21';
  } else if (type === 'steel') {
    return '#9eb7b8';
  } else if (type === 'water') {
    return '#4592c4';
  }
}
